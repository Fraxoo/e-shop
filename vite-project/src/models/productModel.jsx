export class Product {
    constructor(id, title, description, category, price,rating, brand, tags, availabilityStatus, reviews, images) {
        this.id = id;
        this.title = title;
        this.description = description;
        this.category = category;
        this.price = price;
        this.rating = rating;
        this.brand = brand;
        this.tags = tags;
        this.availabilityStatus = availabilityStatus;
        this.reviews = reviews;
        this.images = images;
    }




}
